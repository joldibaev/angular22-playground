import { writeFile } from 'node:fs/promises';
import { features } from 'web-features';
import { browserSupportProfiles, featureSources, nodeMinimums } from './browser-support.config.mjs';

const browserKeys = ['chrome', 'edge', 'firefox', 'safari'];
const featureIds = [...new Set(Object.values(browserSupportProfiles).flat())];

function resolveFeature(id) {
  const [sourceId, compatKey] = featureSources[id] ?? [id];
  const source = features[sourceId];

  if (!source || source.kind !== 'feature') {
    throw new Error(`Unknown web-features id: ${sourceId}`);
  }

  const status = compatKey ? source.status.by_compat_key?.[compatKey] : source.status;
  if (!status) {
    throw new Error(`Unknown compatibility key: ${sourceId}#${compatKey}`);
  }

  return {
    name: compatKey ? source.name : source.name,
    baseline: status.baseline ?? null,
    baselineLowDate: status.baseline_low_date ?? null,
    baselineHighDate: status.baseline_high_date ?? null,
    support: {
      ...Object.fromEntries(
        browserKeys.map((browser) => [browser, status.support[browser] ?? null]),
      ),
      node: nodeMinimums[id] ?? null,
    },
  };
}

const data = Object.fromEntries(featureIds.map((id) => [id, resolveFeature(id)]));
const output =
  `// Generated by npm run generate:browser-support. Do not edit manually.\n` +
  `export const browserSupportFeatures = ${JSON.stringify(data, null, 2)} as const;\n\n` +
  `export const browserSupportProfiles = ${JSON.stringify(browserSupportProfiles, null, 2)} as const;\n`;

await writeFile(
  new URL('../src/app/showcase/browser-support/browser-support.data.ts', import.meta.url),
  output,
);
